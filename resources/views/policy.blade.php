Mua sẽ nhận được hàng đúng hẹn trong Thời Gian K99 Đảm Bảo.
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		13.3&nbsp;&nbsp;&nbsp;&nbsp; Người Sử Dụng hiểu rằng Người Bán chịu toàn
bộ rủi ro liên quan đến việc vận chuyển hàng hóa được mua và bảo đảm rằng Người
Bán đã hoặc sẽ mua bảo hiểm hàng hóa, bao gồm cả việc vận chuyển. &nbsp;Trong
trường hợp Hàng hóa được mua bị hư hỏng, thất lạc hoặc không chuyển phát được
trong quá trình vận chuyển, Người Sử Dụng thừa nhận và đồng ý rằng K99 sẽ
không chịu trách nhiệm đối với bất kỳ tổn thất, chi phí, phí tổn hoặc bất kỳ
khoản phí nào phát sinh từ sự cố đó và Người Bán và/hoặc Người Mua sẽ liên hệ
với đơn vị vận chuyển để giải quyết sự cố đó.
	</p>
