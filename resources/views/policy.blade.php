Bán đồng ý rằng K99 có thể, theo toàn quyền quyết định của mình, thực hiện
các hoạt động khuyến mãi để hỗ trợ các giao dịch giữa Người Bán và Người Mua
thông qua việc giảm, chiết khấu hoặc hoàn lại phí hoặc thông qua những cách
khác. &nbsp;Giá cuối cùng Người Mua cần thanh toán thực tế là giá đã áp dụng
những điều chỉnh trên.
