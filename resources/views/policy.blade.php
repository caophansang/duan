bản thử nghiệm mà các phiên bản này có thể không đúng hoặc không hoàn toàn
giống với phiên bản cuối cùng. K99 có toàn quyền giới hạn một số tính năng
hoặc phạm vi truy cập của bạn đối với một phần hoặc toàn bộ Trang K99 hoặc
Dịch Vụ của K99 mà không cần thông báo hay chịu trách nhiệm.
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		1.5&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; K99 bảo lưu quyền từ chối yêu cầu mở
