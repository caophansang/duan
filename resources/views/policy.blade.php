Mua. Trừ trường hợp có thỏa thuận khác với K99, Người Bán có trách nhiệm
cung cấp đầy đủ thông tin tài khoản ngân hàng để K99 tiến hành chuyển tiền
thanh toán cho đơn hàng đó.
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
