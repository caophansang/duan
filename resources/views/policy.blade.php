cá nhân của Người Sử Dụng khác thông qua việc sử dụng Dịch Vụ (“Bên Nhận Thông
Tin”) theo đây đồng ý rằng, mình sẽ (i) tuân thủ mọi qui định pháp luật về bảo
vệ an toàn thông tin cá nhân liên quan đến những thông tin đó; (ii) cho phép
Người Sử Dụng là chủ sở hữu của các thông thông tin cá nhân mà Bên Nhận Thông
Tin thu thập được (“Bên Tiết Lộ Thông Tin”) được phép xóa bỏ thông tin của mình
được thu thập từ cơ sở dữ liệu của Bên Nhận Thông Tin; và (iii) cho phép Bên
Tiết Lộ Thông Tin rà soát những thông tin đã được thu thập về họ bởi Bên Nhận
Thông Tin, phù hợp với hoặc theo yêu cầu của các qui định pháp luật hiện hành.
