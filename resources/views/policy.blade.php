	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		2.1&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; K99 coi trọng việc bảo mật thông tin
của bạn. Để bảo vệ quyền lợi người dùng, K99 cung cấp Chính Sách Bảo Mật tại
K99.vn để giải thích chi tiết các hoạt động bảo mật của K99. Vui lòng
tham khảo Chính Sách Bảo Mật để biết cách thức K99 thu thập và sử dụng thông
tin liên quan đến Tài Khoản và/hoặc việc sử dụng Dịch Vụ của Người Sử Dụng
(“Thông Tin Người Sử Dụng”). Điều Khoản Dịch Vụ này có liên quan mật thiết với
Chính Sách Bảo Mật. Bằng cách sử dụng Dịch Vụ hoặc cung xấp thông tin trên
Trang K99, Người Sử Dụng:
	</p>
	<p class="MsoNormal" style="margin-left:1.0in;text-indent:-.5in;background:white;">
