	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		3.1&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; K99 trao cho Người Sử Dụng quyền phù
hợp để truy cập và sử dụng các Dịch Vụ theo các điều khoản và điều kiện được
quy định trong Điều Khoản Dịch Vụ này. Tất cả các Nội Dung, thương hiệu, nhãn
hiệu dịch vụ, tên thương mại, biểu tượng và tài sản sở hữu trí tuệ khác độc
quyền (“Tài Sản Sở Hữu Trí Tuệ”) hiển thị trên Trang K99 đều thuộc sở hữu
của K99 và bên sở hữu thứ ba, nếu có. Không một bên nào truy cập vào Trang
K99 được cấp quyền hoặc cấp phép trực tiếp hoặc gián tiếp để sử dụng hoặc
sao chép bất kỳ Tài Sản Sở Hữu Trí Tuệ nào, cũng như không một bên nào truy cập
vào Trang K99 được phép truy đòi bất kỳ quyền, quyền sở hữu hoặc lợi ích nào
liên quan đến Tài Sản Sở Hữu Trí Tuệ. Bằng cách sử dụng hoặc truy cập Dịch Vụ,
bạn đồng ý tuân thủ các quy định pháp luật liên quan đến bản quyền, thương
hiệu, nhãn hiệu dịch vụ hoặc bất cứ quy định pháp luật nào khác bảo vệ Dịch Vụ,
Trang K99 và Nội Dung của Trang K99. Bạn đồng ý không được phép sao chép,
phát tán, tái bản, chuyển giao, công bố công khai, thực hiện công khai, sửa
đổi, phỏng tác, cho thuê, bán, hoặc tạo ra các sản phẩm phái sinh của bất cứ
phần nào thuộc Dịch Vụ, Trang K99 và Nội Dung của Trang K99. Bạn không
được nhân bản hoặc chỉnh sửa bất kỳ phần nào hoặc toàn bộ nội dung của Trang
K99 trên bất kỳ máy chủ hoặc như là một phần của bất kỳ website nào khác mà
chưa nhận được sự chấp thuận bằng văn bản của K99. Ngoài ra, bạn đồng ý rằng
bạn sẽ không sử dụng bất kỳ robot, chương trình do thám (spider) hay bất kỳ
thiết bị tự động hoặc phương thức thủ công nào để theo dõi hoặc sao chép Nội
Dung của K99 khi chưa có sự đồng ý trước bằng văn bản của K99 (sự chấp
thuận này được xem như áp dụng cho các công cụ tìm kiếm cơ bản trên các webside
tìm kiến trên mạng kết nối người dùng trực tiếp đến website đó).
