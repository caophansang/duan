		<b>1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; GIỚI THIỆU</b>
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;background:white;">
		Điều khoản dịch vụ này
là một phần không thể tách rời của “Quy Chế Hoạt Động Sàn Giao Dịch Thương Mại
Điện Tử K99”. Tham khảo toàn văn Quy Chế Hoạt Động tại&nbsp;<a href="#">đây</a><u>.</u>
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		1.1&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Chào mừng bạn đến với sàn giao dịch
thương mại điện tử K99 qua giao diện website hoặc ứng dụng di động (“Trang
K99”). Trước khi sử dụng Trang K99 hoặc tạo tài khoản K99 (“Tài
Khoản”), vui lòng đọc kỹ các&nbsp;<b>Điều Khoản Dịch Vụ&nbsp;</b>dưới đây
và&nbsp;<b>Quy Chế Hoạt Động Sàn Giao Dịch Thương Mại Điện Tử K99</b>&nbsp;để
hiểu rõ quyền lợi và nghĩa vụ hợp pháp của mình đối với Công ty TNHH K99 và
các công ty liên kết và công ty con của K99 (sau đây được gọi riêng là “K99”,
gọi chung là “chúng tôi”, “của chúng tôi”). “Dịch Vụ” chúng tôi cung cấp hoặc
sẵn có bao gồm (a) Trang K99, (b) các dịch vụ được cung cấp bởi Trang K99
và bởi phần mềm dành cho khách hàng của K99 có sẵn trên Trang K99, và (c)
tất cả các thông tin, đường dẫn, tính năng, dữ liệu, văn bản, hình ảnh, biểu
đồ, âm nhạc, âm thanh, video (bao gồm cả các đoạn video được đăng tải trực tiếp
theo thời gian thực (livestream)), tin nhắn, tags, nội dung, chương trình, phần
mềm, ứng dụng dịch vụ (bao gồm, nhưng không giới hạn, bất kỳ ứng dụng dịch vụ
di động nào) hoặc các tài liệu khác có sẵn trên Trang K99 hoặc các dịch vụ
liên quan đến Trang K99 (“Nội Dung”). Bất kỳ tính năng mới nào được bổ sung
hoặc mở rộng đối với Dịch Vụ đều thuộc phạm vi điều chỉnh của Điều Khoản Dịch
Vụ này. Điều Khoản Dịch Vụ này điều chỉnh việc&nbsp; bạn sử dụng Dịch Dụ cung
cấp bởi K99.
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		1.2&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Dịch Vụ bao gồm dịch vụ sàn giao dịch
trực tuyến kết nối người tiêu dùng với nhau nhằm mang đến cơ hội kinh doanh
giữa người mua (“Người Mua”) và người bán (“Người Bán”) (gọi chung là “bạn”,
“Người Sử Dụng” hoặc “Các Bên”). Hợp đồng mua bán thật sự là trực tiếp giữa
Người Mua và Người Bán. Các Bên liên quan đến giao dịch đó sẽ hoàn toàn chịu
trách nhiệm đối với hợp đồng mua bán giữa họ, việc đăng bán hàng hóa, bảo hành
sản phẩm và tương tự. K99 không can thiệp vào giao dịch giữa các Người Sử
Dụng. K99 có thể hoặc không sàng lọc trước Người Sử Dụng hoặc Nội Dung hoặc
thông tin cung cấp bởi Người Sử Dụng. K99 bảo lưu quyền loại bỏ bất cứ Nội
Dung hoặc thông tin nào trên Trang K99 theo&nbsp; Mục 6.4 bên dưới. K99
không bảo đảm cho việc các Người Sử Dụng sẽ thực tế hoàn thành giao dịch. Lưu
ý, K99 sẽ là bên trung gian quản lý tình trạng hàng hóa và mua bán giữa
Người Mua và Người Bán và quản lý vấn đề chuyển phát, trừ khi Người Mua và
Người Bán thể hiện mong muốn tự giao dịch với nhau một cách rõ ràng.
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		1.3&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Trước khi trở thành Người Sử Dụng của
Trang K99, bạn cần đọc và chấp nhận mọi điều khoản và điều kiện được quy
định trong, và dẫn chiếu đến, Điều Khoản Dịch Vụ này và Chính Sách Bảo Mật được
dẫn chiếu theo đây.
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		1.4&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; K99 bảo lưu quyền thay đổi, chỉnh sửa,
tạm ngưng hoặc chấm dứt tất cả hoặc bất kỳ phần nào của Trang K99 hoặc Dịch
Vụ vào bất cứ thời điểm nào theo qui định pháp luật. K99 không chịu trách
