K99 quyết định, thay đổi và chỉnh sửa vào từng thời điểm, tùy thuộc vào hạn
mức theo toàn quyền quyết định của K99, Người Sử Dụng có thể sử dụng K99
Xu bằng cách gửi yêu cầu đến K99 và sử dụng K99 Xu để khấu trừ vào giá
sản phẩm khi thực hiện giao dịch mua hàng trên Trang K99 theo quy định của
K99 vào từng thời điểm. &nbsp;Tất cả các khoản hoàn tiền phải tuân thủ theo
Chính Sách Trả Hàng và Hoàn Tiền tại Mục 14.4.
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		12.5&nbsp;&nbsp;&nbsp;&nbsp; K99 Xu mà bạn đã sử dụng sẽ được trừ vào
số dư K99 Xu. &nbsp;Mỗi K99 Xu đều có hạn sử dụng. &nbsp;Lưu ý kiểm tra
chi tiết Tài Khoản của bạn trên Trang K99 để biết số dư K99 Xu cũng như
hạn sử dụng.
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		12.6&nbsp;&nbsp;&nbsp;&nbsp; Vào từng thời điểm, K99 sẽ thông tin đến
bạn điểm thưởng K99 Xu sẽ được áp dụng đối với Hoạt Động Hợp Lệ cụ thể
nào.&nbsp; Hoạt Động Hợp Lệ có thể bao gồm nhưng không giới hạn đối với việc
bạn thực hiện mua sắm với Người Bán đang tham gia chương trình hoặc các chương
trình khuyến mãi cụ thể của K99. &nbsp;Chúng tôi sẽ thông báo đến bạn các
điều khoản của các chương trình điểm thưởng đó nếu được áp dụng vào từng thời
điểm.
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		12.7&nbsp;&nbsp;&nbsp;&nbsp; Nếu bạn có khiếu nại liên quan đến số lượng
K99 Xu nhận được từ Hoạt Động Hợp Lệ, thì khiếu nại đó phải được gửi đến
K99 (<a href="#"><b>tại đây</b></a>)&nbsp;trong vòng một
(01) tháng kể từ ngày bạn thực hiện thành công giao dịch. Chúng tôi có thể yêu
cầu bạn cung cấp bằng chứng để hỗ trợ cho việc khiếu nại của bạn.&nbsp; K99
bảo lưu quyền từ chối giải quyết khiếu nại nếu thời gian gửi khiếu nại đã qua.
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		12.8&nbsp;&nbsp;&nbsp;&nbsp; K99 không đảm bảo và không chấp nhận
trách nhiệm thực hiện nghĩa vụ thuế cho K99 Xu. &nbsp;Bạn cần kiểm tra với
chuyên viên tư vấn thuế của bạn xem là việc nhận K99 Xu có ảnh hưởng đến
nghĩa vụ thuế của bạn hay không.
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
		12.9&nbsp;&nbsp;&nbsp;&nbsp; Trong giới hạn quy định pháp luật áp dụng,
K99 bảo lưu quyền (i) ngưng hệ thống điểm thưởng K99 Xu bất cứ lúc nào,
và (ii) hủy hoặc hoãn quyền tham gia vào hệ thống điểm thưởng K99 Xu của
Người Sử Dụng, bao gồm cả khả năng có được hoặc sử dụng K99 Xu.
	</p>
	<p class="MsoNormal" style="text-align:justify;background:white;">
		<b>&nbsp;</b>
