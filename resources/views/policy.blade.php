bố và/hoặc xử lý các Nội Dung, dữ liệu cá nhân của bạn và Thông Tin Người Sử
Dụng như được quy định trong Chính Sách Bảo Mật;
	</p>
	<p class="MsoNormal" style="margin-left:1.0in;text-align:justify;text-indent:-.5in;background:white;">
		b.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; đồng ý và công nhận rằng, trong phạm vi
pháp luật có liên quan cho phép, các Thông Tin Người Sử Dụng sẽ thuộc sở hữu
chung của bạn và K99; và
	</p>
	<p class="MsoNormal" style="margin-left:1.0in;text-align:justify;text-indent:-.5in;background:white;">
		c.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; sẽ không, dù là trực tiếp hay gián
tiếp, tiết lộ các Thông Tin Người Sử Dụng cho bất kỳ bên thứ ba nào, hoặc bằng
bất kỳ phương thức nào cho phép bất kỳ bên thứ ba nào được truy cập hoặc sử
dụng Thông Tin Người Dùng của bạn.
	</p>
	<p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;background:white;">
