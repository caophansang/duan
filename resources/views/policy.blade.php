gửi từ nước ngoài, sản phẩm đó được bán bởi Người Bán ngoài Việt Nam, và việc
xuất/nhập khẩu sản phẩm đó chịu sự điều chỉnh của pháp luật nước sở tại.&nbsp;
Người Sử Dụng cần hiểu rõ các hạn chế về xuất/nhập khẩu hàng hóa của quốc gia
nhập khẩu.&nbsp; Người Sử Dụng đồng ý rằng K99 không thể tư vấn pháp lý liên
quan đến vấn đề này và đồng ý rằng K99 sẽ không thể chịu các rủi ro hoặc
trách nhiệm pháp lý đối với hoạt động xuất/nhập khẩu hàng hóa vào Việt Nam.
